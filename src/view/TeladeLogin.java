    private void status(){
        try{
            if(conexao != null){
                lblStatus.setText("conectado");
            } else {
                lblStatus.setText("desconectado");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
        try{
            conexao.close();
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
}
    
